void main() {
  var month = 'sep';
  switch(month){
    case 'jan' : print('month is jan');
    break;
    case 'feb' : print('month is feb');
    break;
    case 'march': print('month is march');
    break;
    case 'sep'  : print('month is sep');
    break;
    default: print('none of these');
  }
}